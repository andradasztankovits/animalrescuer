package com.fasttrackit;

public class Pet {

    String name;
    int age;
    String health;
    String food;
    String game;
    String hungerSensation;
    String mood;


    public Pet (String name, int age, String health, String food, String game, String hungerSensation, String mood){

    this.name = name;
    this.age = age;
    this.health = health;
    this.food = food;
    this.game = game;
    this.hungerSensation = hungerSensation;
    this.mood = mood;

}

}